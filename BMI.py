"""
Susan has a weight of 60 kilograms and a height of 1.58 meters. Write Code to Calculate and Print Susan’s Body Mass Index as float. Note: Body Mass Index is a person’s weight in kilograms divided by the square of height in meters.
"""
#Store weight and height of Susan
weight=60
height=1.58

#Calculate BMI
BMI=float(weight/(height*height))

#Print BMI
print(BMI)
